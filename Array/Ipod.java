class Ipod {
    void playMusic() {
        System.out.println("Playing music");
    }

    void pauseMusic() {
        System.out.println("Music paused");
    }

    void nextSong() {
        System.out.println("Next song");
    }

    void previousSong() {
        System.out.println("Previous song");
    }

    void increaseVolume() {
        System.out.println("Volume increased");
    }
}