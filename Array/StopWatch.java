class StopWatch {
    void startTimer() {
        System.out.println("Timer started");
    }

    void stopTimer() {
        System.out.println("Timer stopped");
    }

    void resetTimer() {
        System.out.println("Timer reset");
    }

    void showTime() {
        System.out.println("Displaying time");
    }

    void pauseTimer() {
        System.out.println("Timer paused");
    }
}